import turtle

t=turtle.Turtle()

def hello(a):
	a.pencolor("red")
	a.pensize(10)
	a.lt(90)
	a.fd(200)
	a.pu()
	a.goto(0, 100)
	a.rt(90)
	a.pd()
	a.circle(-50, extent=90)
	a.fd(50)
	a.pu()
	a.goto(120,0)
	a.lt(90)
	a.pencolor("orange")
	a.pd()
	a.circle(50, extent=-270)
	a.rt(90)
	a.bk(100)
	a.pencolor("yellow")
	a.pu()
	a.goto(190, 0)
	a.pd()
	a.lt(90)
	a.fd(200)
	a.pencolor("green")
	a.pu()
	a.goto(210, 0)
	a.pd()
	a.fd(200)
	a.pencolor("blue")
	a.pu()
	a.goto(280, 0)
	a.rt(90)
	a.pd()
	a.circle(50)
	a.pu()
	a.goto(0, 0)

while True:
	hello(t)
